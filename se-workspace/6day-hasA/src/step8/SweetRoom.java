package step8;

public class SweetRoom {
	private String number;
	private int price;
	
	SweetRoom(String number, int price)
	{
		this.number = number;
		this.price = price;
	}
	
	public String getNumber()
	{
		return this.number;
		
	}
	
	public int getPrice()
	{
		return this.price;
		
	}
}
